    private void populateVolunteerTable() {
        DefaultTableModel tableModel = (DefaultTableModel) tblVolunteerData.getModel();
        tableModel.setRowCount(0);
        organization=network.getEnterpriseDirectory().findEnterprise("Non-Profit Enterprise").getOrganizationDirectory().findOrganization("Volunteer Management");
        for (VolunteerProfile vp : organization.getVolunteerDirectory().getVolunteerList()) {
                Object[] row ={
                            vp.getName(),
                            vp.getEmail(),
                            vp.getAge(),
                            vp.getTotalHours()
                        };
                        tableModel.addRow(row);        }
        txtVolunteerCount.setText(Integer.toString(organization.getVolunteerDirectory().getVolunteerList().size()));
    }

    private void populateReceipientsTable() {
        DefaultTableModel tableModel = (DefaultTableModel) tblReceipientsData.getModel();
        tableModel.setRowCount(0);
        organization = network.getEnterpriseDirectory().findEnterprise("Public Service Enterprise").getOrganizationDirectory().findOrganization("Receipient Registration");
        for (AidReceipentProfile vp : organization.getAidReceipentDirectory().getAidReceipentsList()) {
                Object[] row ={
                            vp.getName(),
                            vp.getEmail(),
                            vp.getAge()
                        };
                        tableModel.addRow(row);        }
        txtReceipientsCount.setText(Integer.toString(organization.getAidReceipentDirectory().getAidReceipentsList().size()));
    }

    private void populateCampaignTable() {
        DefaultTableModel tableModel = (DefaultTableModel) tblCampaignData.getModel();
        tableModel.setRowCount(0);
        organization = network.getEnterpriseDirectory().findEnterprise("Corporate Donors Enterprise").getOrganizationDirectory().findOrganization("Campaign Management");
        if (organization == null) {
    System.err.println("Organization 'Campaign Management' not found!");
    return; // Exit the method to avoid further errors
}
        if (organization.getCampaignDirectory().getAllCampaigns() == null) {
    System.err.println("No campaigns found for organization: " + organization.getName());
    return; // Exit the method
}

        for (Campaign c : organization.getCampaignDirectory().getAllCampaigns()) {
                Object[] row ={
                            c.getName(),
                            c.getDescription(),
                            c.getLocation(),
                            c.getFromDate(),
                            c.getToDate()
                        };
                        tableModel.addRow(row);        }
        txtCampaignCount.setText(Integer.toString(organization.getCampaignDirectory().getAllCampaigns().size()));
    }
